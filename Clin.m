function C = Clin(x)
    r = sqrt(x(1)^2 + x(3)^2 + x(5)^2);
    y11 = x(1)/r; y13 = x(3)/r; y15 = x(5)/r;
    y21 = -x(3)/(x(1)^2 + x(3)^2); y23 = x(1)/(x(1)^2 + x(3)^2);
    y31 = -x(5)*x(1)/(r^2*sqrt(x(1)^2 + x(3)^2)); y33 = -x(5)*x(3)/(r^2*sqrt(x(1)^2 + x(3)^2)); y35 = sqrt(x(1)^2 + x(3)^2)/r^2;
    C = [y11 0 y13 0 y15 0 0;
         y21 0 y23 0 0 0 0;
         y31 0 y33 0 y35 0 0];
end